function getValueByKey(lang, key) {
  const bundle = TRANSLATIONS[lang] || TRANSLATIONS[DEFAULT_LANGUAGE];
  return bundle[key] ?? TRANSLATIONS[DEFAULT_LANGUAGE][key] ?? null;
}

export function t(key, params = {}, lang = getSettings().language || DEFAULT_LANGUAGE) {
  const value = getValueByKey(lang, key);
  if (value === null) return null;
  return Object.entries(params).reduce((str, [paramKey, paramValue]) => {
    const placeholder = `{${paramKey}}`;
    return str.split(placeholder).join(String(paramValue));
  }, value);
}

export function setDocumentLanguage(lang) {
  if (typeof document === 'undefined') return;
  document.documentElement.lang = lang;
}

function getPageKey(root) {
  if (!root || typeof window === 'undefined') return 'settings';
  const pageKey = root.dataset.page;
  if (pageKey) return pageKey;

  const filename = window.location.pathname.split('/').pop() || 'index.html';
  switch (filename) {
    case 'index.html':
    case '':
      return 'home';
    case 'asmr.html':
      return 'asmr';
    case 'attendance.html':
      return 'attendance';
    case 'calendar.html':
      return 'calendar';
    case 'friends.html':
      return 'friends';
    case 'gallery.html':
      return 'gallery';
    case 'sleep.html':
      return 'sleep';
    case 'workshop.html':
      return 'workshop';
    case 'worry.html':
      return 'worry';
    case 'minigame.html':
      return 'minigame';
    case 'settings.html':
      return 'settings';
    default:
      return 'settings';
  }
}

export function applyTranslations(lang = getSettings().language || DEFAULT_LANGUAGE, root = document.body) {
  if (!root) return;
  setDocumentLanguage(lang);
  const pageKey = getPageKey(root);

  const resolveTranslation = (key) => {
    const direct = t(key, {}, lang);
    if (direct !== null) return direct;
    // Try page-scoped key (e.g. page.heading.home)
    if (key.startsWith('page.')) {
      const pageScoped = t(`${key}.${pageKey}`, {}, lang);
      if (pageScoped !== null) return pageScoped;
    }
    // If no translation found for requested lang, try DEFAULT_LANGUAGE as fallback
    if (lang !== DEFAULT_LANGUAGE) {
      const fallback = t(key, {}, DEFAULT_LANGUAGE);
      if (fallback !== null) return fallback;
      if (key.startsWith('page.')) {
        const pageScopedFallback = t(`${key}.${pageKey}`, {}, DEFAULT_LANGUAGE);
        if (pageScopedFallback !== null) return pageScopedFallback;
      }
    }
    return null;
  };

  const elements = root.querySelectorAll('[data-i18n], [data-i18n-html], [data-i18n-placeholder], [data-i18n-title], [data-i18n-alt], [data-i18n-aria-label]');
  elements.forEach(el => {
    const htmlKey = el.dataset.i18nHtml;
    const textKey = el.dataset.i18n;
    const placeholderKey = el.dataset.i18nPlaceholder;
    const titleKey = el.dataset.i18nTitle;
    const altKey = el.dataset.i18nAlt;
    const ariaLabelKey = el.dataset.i18nAriaLabel;

    if (htmlKey) {
      const value = resolveTranslation(htmlKey);
      if (value !== null) el.innerHTML = value;
    }

    if (textKey) {
      const value = resolveTranslation(textKey);
      if (value !== null) el.textContent = value;
    }

    if (placeholderKey) {
      const value = resolveTranslation(placeholderKey);
      if (value !== null) el.placeholder = value;
    }

    if (titleKey) {
      const value = resolveTranslation(titleKey);
      if (value !== null) el.title = value;
    }

    if (altKey) {
      const value = resolveTranslation(altKey);
      if (value !== null) el.alt = value;
    }

    if (ariaLabelKey) {
      const value = resolveTranslation(ariaLabelKey);
      if (value !== null) el.setAttribute('aria-label', value);
    }
  });

  const pageTitle = resolveTranslation('page.title') || t(`page.title.${pageKey}`, {}, lang);
  if (pageTitle) {
    document.title = pageTitle;
    const titleElement = document.querySelector('title[data-i18n]');
    if (titleElement) {
      titleElement.textContent = pageTitle;
    }
  }
}
